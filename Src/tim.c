/**
  * @brief  Clears the enable bit and counter register for all of the timers
  * @return None
  */
void resetTimers(void)
{
  CLEAR_BIT(transformer.trigTIM_Handle->Instance->CR1, TIM_CR1_CEN);
  CLEAR_BIT(switch1.gateTIM_Handle->Instance->CR1, TIM_CR1_CEN);
  CLEAR_BIT(switch1.trigTIM_Handle->Instance->CR1, TIM_CR1_CEN);
  CLEAR_BIT(switch2.gateTIM_Handle->Instance->CR1, TIM_CR1_CEN);
  CLEAR_BIT(switch2.trigTIM_Handle->Instance->CR1, TIM_CR1_CEN);
  CLEAR_REG(transformer.trigTIM_Handle->Instance->CNT); 
  CLEAR_REG(switch1.gateTIM_Handle->Instance->CNT); 
  CLEAR_REG(switch1.trigTIM_Handle->Instance->CNT);
  CLEAR_REG(switch2.gateTIM_Handle->Instance->CNT);
  CLEAR_REG(switch2.trigTIM_Handle->Instance->CNT);
}
