/**
  * @brief  Starts all of the timers - Critical timers first then debug OCs
  * @return None
  */
void startTimers(void)
{
  HAL_TIM_Base_Start(switch1.trigTIM_Handle);
  HAL_TIM_Base_Start(switch2.trigTIM_Handle);
  HAL_TIM_PWM_Start(switch1.gateTIM_Handle, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start_IT(switch1.gateTIM_Handle, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(switch2.gateTIM_Handle, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start_IT(switch2.gateTIM_Handle, TIM_CHANNEL_2);
  HAL_TIM_Base_Start(transformer.trigTIM_Handle);

  HAL_TIM_OC_Start_IT(switch1.gateTIM_Handle, TIM_CHANNEL_3);
  HAL_TIM_OC_Start(switch1.trigTIM_Handle, TIM_CHANNEL_1);
  HAL_TIM_OC_Start(switch2.trigTIM_Handle, TIM_CHANNEL_1);
  HAL_TIM_OC_Start_IT(transformer.trigTIM_Handle, TIM_CHANNEL_1);
}

